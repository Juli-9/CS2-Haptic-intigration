PhatomSensation::PhatomSensation(uint8_t pin1, uint8_t pin2)
{
    motor1_pin = pin1;
    motor2_pin = pin2;

    pinMode(motor1_pin, OUTPUT);
    pinMode(motor2_pin, OUTPUT);

    off();
}

void PhatomSensation::off(){
    analogWrite(motor1_pin, 0);
    analogWrite(motor2_pin, 0);
}

void PhatomSensation::update_position(float pos)
{
    position = constrain(pos, 0.0f, 1.0f);
}

void PhatomSensation::update_intensity(float intens){
    intensity = constrain(intens, 0.0f, 1.0f);
}

void PhatomSensation::update_pattern(PhatomSensation::Pattern p){
    pattern = p;
}

void PhatomSensation::process(){
    float pos = position;
    float gain1 = pos;
    float gain2 = 1.0 - pos;

    // float -> 8 Bit
    uint8_t idx1 = (uint8_t)(gain1 * 255.0f);
    uint8_t idx2 = (uint8_t)(gain2 * 255.0f);

    // logarithmische LUT
    uint8_t pwm1 = logLUT[idx1] * intensity * pattern_bias();
    uint8_t pwm2 = logLUT[idx2] * intensity * pattern_bias();

    Serial.println(pwm1);
    Serial.println(pwm2);
    Serial.println("------");

    analogWrite(motor1_pin, pwm1);
    analogWrite(motor2_pin, pwm2);
}

